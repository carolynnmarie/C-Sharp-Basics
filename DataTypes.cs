using System;

namespace C_Sharp_Basics{

    public class DataTypes{

        public static float dataTypeFloat(){
            float a = 7;
            float b = 4;
            float c = 3;
            float d = (a+b)/c;
            return d;
        }

        public static decimal dataTypeDecimal(){
            decimal a = 7;
            decimal b = 4;
            decimal c = 3;
            decimal d = (a+b)/c;
            return d;
        }

        public static double dataTypeDouble(){
            double a = 7;
            double b = 4;
            double c = 3;
            double d = (a+b)/c;
            return d;
        }

        public static int dataTypeInt(){
            int a = 7;
            int b = 4;
            int c = 3;
            int d = (a+b)/c;
            return d;
        }

        public static int dataTypeIntRemainder(){
            int a = 7;
            int b = 4;
            int c = 3;
            int d = (a+b)%c;
            return d;
        }
    }
}